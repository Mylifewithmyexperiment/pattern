package com.sterlite.dccmappfordealersterlite.model;

public class PlanBenefit {
    private String benefit_Image;
    private String title;
    private String description;

    public String getBenefit_Image() {
        return benefit_Image;
    }

    public void setBenefit_Image(String benefit_Image) {
        this.benefit_Image = benefit_Image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
